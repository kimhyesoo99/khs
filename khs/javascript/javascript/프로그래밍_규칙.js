/*

    정수1 : 10
    정수2 : 5

    10 + 5 = 15

 */


let n1, n2;

n1 = +prompt('정수1 : ');                               //   prompt > 브라우저 입력 대화상자 창 활성화
n2 = +prompt('정수2 : ');                               //    + 사용하면 문자열에서 바뀜 형변환 

alert(`${n1} + ${n2} = ${n1 + n2}`)

